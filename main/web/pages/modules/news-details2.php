                                                        <h2 class="entry-title">
                                                            Solicitan retirar del cargo al jefe de la Segunda Zona de Carabineros en Antofagasta por presuntas denuncias de abuso sexual
                                                        </h2>
                                                        <div class="entry-content">
                                                            <p><strong>El general director de Carabineros, Bruno Villalobos, solicitó cursar el retiro del jefe de la Segunda Zona de Antofagasta, el general Víctor Acosta Contreras, por presuntas denuncias de abuso sexual.</strong></p>
                                                            <p>Información que salió publicada en el portal de internet de radio Cooperativa y en el diario electrónico El Mostrador, afirmando que la solicitud va dirigida al ministro del Interior, Mario Fernández, y en ella se explica que “la decisión de la máxima autoridad institucional se funda en la permanente evaluación de sus mandos, de las competencias asociadas a sus cargos y, en este caso en particular, a la falta de oportunidad para informar hechos y situaciones que en razón de sus obligaciones funcionarias debió haber concretado”.
                                                                Según fuentes ligadas a la Institución, el general Villalobos, tomó la determinación durante las últimas horas, luego de que se dieran a conocer más de una denuncia por abuso sexual en contra del general Acosta.
                                                                Tras estos hechos, nos contactamos con el encargado del departamento de comunicaciones de Carabineros, quien nos explicó que esperan una respuesta del Ministerio del Interior para dar declaraciones al respecto, para afirmar o desmentir dichas acusaciones, y que por el momento solo debemos abocarnos al comunicado que envió la institución.
                                                                Acosta ahora será investigado por la Fiscalía local.
                                                            </p>

