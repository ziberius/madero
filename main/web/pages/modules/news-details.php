                                                        <h2 class="entry-title">
                                                            Holanda apoyará al Gobierno Regional de Coquimbo para mejorar administración y uso eficiente del agua
                                                        </h2>
                                                        <div class="entry-content">
                                                            <p>El proceso de desertificación por el que atraviesa la región de Coquimbo, y los extensos periodos de escasez hídrica, implican un importante desafío para el Gobierno Regional, en la implementación de una estrategia de gestión hídrica coherente con el territorio, donde la falta de agua es estructural.

                                                                Durante el desarrollo del Congreso del futuro se realizó una importante reunión de trabajo entre la embajadora de Holanda en Chile, Marion Kappeyne; el embajador del agua en Holanda Henk Ovink; el intendente Claudio Ibáñez; la senadora Adriana Muñoz y distintos actores regionales públicos en materia hídrica. Durante el encuentro, se generó un importante acuerdo para profundizar los esfuerzos locales en materia de eficiencia hídrica con el apoyo de los Países Bajos.

                                                                El Gobierno de Holanda, reconocido mundialmente por su experiencia en administración del agua, se comprometió a colaborar con proyectos de alcance nacional y regional, como son la creación del Instituto del Agua, la generación de diálogo permanente con la comunidad sobre los cambios necesarios en materia de la gestión del recurso hídrico y un programa piloto de gestión integrada del recurso hídrico en la cuenca del río Elqui, además de la posible colaboración con el piloto ya en desarrollo para el río Choapa.

                                                                El intendente Claudio Ibáñez señaló cuál es el objetivo para mejorar el sistema hídrico en la región de Coquimbo. La embajadora de Holanda, Marion Kappeyne, detalló que la región de Coquimbo tiene muchos desafíos. 
                                                                La senadora Adriana Muñoz señaló que se viene trabajando para concretar este convenio que permitirá construir las bases de un modelo de gestión del recurso hídrico.
                                                                Henk Ovink, embajador del agua de Holanda y asesor en materia hídrica del ex Presidente de EE.UU., Barack Obama, enfatizó en que para lograr un uso eficiente del agua hay que construir capacidades dentro de las instituciones, pero también entre los ciudadanos.
                                                                Una de las mayores problemáticas para hacer un uso y administración eficiente del agua en Chile es la gran cantidad de actores relacionados al agua al interior de cada cuenca. El modelo propuesto por Holanda habla de interactuar de forma conjunta para crear una gobernanza que genere un equilibrio entre el uso minero, agrícola, industrial y para la bebida, todo esto medido a través de un estricto monitoreo.</p>

